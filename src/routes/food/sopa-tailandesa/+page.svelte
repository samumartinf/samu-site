						<span class="text-2xl font-bold text-primary/30 flex-shrink-0 leading-none">6</span>
						<div>
							<p>In the same pan where you seared the chicken, pour one can of coconut milk and scrape the bottom well to lift all the juices. Pour the mixture into the pot, add the second can, and simmer for 20 minutes over medium heat.</p>
						</div>
					</div>
				</li>
